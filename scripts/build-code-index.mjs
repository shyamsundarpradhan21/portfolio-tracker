#!/usr/bin/env node
// build-code-index.mjs — dependency-free, cross-platform symbol index.
//
// WHY: a committed, greppable map of symbol -> file:line so the agent can
// pinpoint definitions without grepping the whole tree, and without relying on
// any external binary or language server.
//
// RUN:  npm run index        (or: node scripts/build-code-index.mjs)
// OUT:  code-index.tsv        columns: symbol <TAB> kind <TAB> path <TAB> line
//
// Regenerate after structural changes. It is intentionally regex-based and
// approximate — a fast orientation aid, not a compiler.

import { readdirSync, statSync, readFileSync, writeFileSync } from 'node:fs';
import { join, relative, sep } from 'node:path';

const ROOT = process.cwd();

// Only these dirs hold source worth indexing.
const INCLUDE_DIRS = ['app', 'scripts', 'mcp', 'audit', 'data'];

// Never descend into these (generated, vendored, or secret-bearing dirs).
const SKIP_DIRS = new Set([
  'node_modules', '.next', '.git', '.playwright-mcp', '_sandbox',
  'build', 'out', '.vercel', '__pycache__', '.venv', 'reports',
  'contract-parser', // has its own .env / secrets; skip
]);

const CODE_EXT = /\.(mjs|js|jsx|ts|tsx|py)$/;

function walk(dir, acc = []) {
  let entries;
  try { entries = readdirSync(dir); } catch { return acc; }
  for (const name of entries) {
    if (SKIP_DIRS.has(name)) continue;
    const full = join(dir, name);
    let st;
    try { st = statSync(full); } catch { continue; }
    if (st.isDirectory()) {
      walk(full, acc);
    } else if (CODE_EXT.test(name)) {
      // data/ holds huge generated *.json (already excluded by ext) — but also
      // a couple of real source .js files (e.g. nifty50.js); those are kept.
      acc.push(full);
    }
  }
  return acc;
}

const PATTERNS = [
  // JS / TS
  [/^\s*export\s+(?:async\s+)?function\s+([A-Za-z0-9_$]+)/, 'export-fn'],
  [/^\s*export\s+default\s+(?:async\s+)?function\s+([A-Za-z0-9_$]+)/, 'export-default-fn'],
  [/^\s*export\s+class\s+([A-Za-z0-9_$]+)/, 'export-class'],
  [/^\s*export\s+const\s+([A-Za-z0-9_$]+)\s*=/, 'export-const'],
  [/^\s*export\s+(?:async\s+)?function\s+([A-Z][A-Za-z0-9_$]*)\s*\(/, 'component'], // Capitalized => likely React component
  [/^\s*(?:async\s+)?function\s+([A-Za-z0-9_$]+)\s*\(/, 'fn'],
  [/^\s*const\s+([A-Za-z0-9_$]+)\s*=\s*(?:async\s*)?\(/, 'const-fn'],
  // Next.js route handlers
  [/^\s*export\s+(?:async\s+)?function\s+(GET|POST|PUT|PATCH|DELETE|HEAD|OPTIONS)\b/, 'route-handler'],
  // Python
  [/^\s*def\s+([A-Za-z0-9_]+)\s*\(/, 'py-def'],
  [/^\s*class\s+([A-Za-z0-9_]+)\b/, 'py-class'],
];

const rows = [];
const files = walk(ROOT);

for (const file of files) {
  const rel = relative(ROOT, file).split(sep).join('/');
  let lines;
  try { lines = readFileSync(file, 'utf8').split('\n'); } catch { continue; }

  // Next.js API route path, derived from the file location.
  if (/^app\/api\/.*\/route\.(js|ts|mjs)$/.test(rel)) {
    const routePath = '/' + rel.replace(/^app\//, '').replace(/\/route\.(js|ts|mjs)$/, '');
    rows.push([routePath, 'api-route', rel, '1']);
  }

  lines.forEach((line, i) => {
    for (const [re, kind] of PATTERNS) {
      const m = line.match(re);
      if (m) { rows.push([m[1], kind, rel, String(i + 1)]); break; }
    }
  });
}

// De-dupe + sort by symbol then path.
const seen = new Set();
const unique = rows.filter((r) => {
  const k = r.join('\t');
  if (seen.has(k)) return false;
  seen.add(k);
  return true;
});
unique.sort((a, b) => a[0].localeCompare(b[0]) || a[2].localeCompare(b[2]));

const header =
  `# code-index.tsv — generated by scripts/build-code-index.mjs (npm run index)\n` +
  `# symbol\tkind\tpath\tline  —  grep this to pinpoint a definition, e.g.  grep -P "^syncBrokers\\t" code-index.tsv\n` +
  `# ${unique.length} symbols across ${files.length} source files. Regenerate after structural changes.\n`;

const body = unique.map((r) => r.join('\t')).join('\n') + '\n';
writeFileSync(join(ROOT, 'code-index.tsv'), header + body);

console.log(`Indexed ${unique.length} symbols across ${files.length} files -> code-index.tsv`);
